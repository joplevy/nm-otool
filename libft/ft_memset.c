#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*temp;

	temp = (unsigned char *)b;
	while (len > 0)
	{
		temp[len - 1] = (unsigned char)c;
		len--;
	}
	return (b);
}
